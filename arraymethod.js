// Array Methods
let num = [1, 2, 3, 34, 4]
let b = num.toString() 
console.log(b, typeof b)
let c = num.join(" and ")
console.log(c, typeof c)
let r1 = num.pop() 
console.log(num, r1)
let t = num.push(56) 
console.log(num, t)
let y = num.shift()
console.log(y, num) 

let r = num.unshift(78)
console.log(r, num)
console.log(r)